# Suma
a = 10
b = 5
suma = a + b
print(f"La suma de {a} y {b} es {suma}")

# Resta
resta = a - b
print(f"La resta de {a} y {b} es {resta}")

# Multiplicación
multiplicacion = a * b
print(f"La multiplicación de {a} y {b} es {multiplicacion}")

# División
division = a / b
print(f"La división de {a} entre {b} es {division}")

# División entera
division_entera = a // b
print(f"La división entera de {a} entre {b} es {division_entera}")

# Módulo
modulo = a % b
print(f"El módulo de {a} entre {b} es {modulo}")

# Potencia
potencia = a ** b
print(f"{a} elevado a la potencia de {b} es {potencia}")
